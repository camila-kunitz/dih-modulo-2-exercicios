namespace BibliotecaClasses
{
    public static class Calculadora
    {
        public static int Somar(int valorA, int valorB)
        {
            return valorA + valorB;
        }

        public static int Somar(int valorA, int valorB, int valorC)
        {
            return valorA + valorB + valorC;
        }

        public static int Subtrair(int valorA, int valorB)
        {
            return valorA - valorB;
        }

        public static int Subtrair(int valorA, int valorB, int valorC)
        {
            return valorA - valorB - valorC;
        }

        public static int Multiplicar(int valorA, int valorB)
        {
            return valorA * valorB;
        }

        public static int Multiplicar(int valorA, int valorB, int valorC)
        {
            return valorA * valorB * valorC;
        }

    }
}
